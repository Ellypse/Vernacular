-- Algo de hash, inspiré de celui de Daniel J. Bernstein
function TRP2_Hash( text )
    local l = strlen( text );
    local h = 5381;
    for i = 1, l, 1 do
        local v = strbyte( text, i );
        h = ( ( h * 33 ) + v );
    end
    return h;
end